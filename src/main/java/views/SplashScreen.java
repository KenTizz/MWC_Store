        private void loadDing() {
        new Thread() {
            public void run() {
                for (int i = 0; i <= 100; i++) {
                    try {
                        sleep(30);
                        lbl_percent.setText("Loading " + i + "%");
                        loadingbar.setValue(i);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                    if (i == 100) {
                        closeWhenDone();
                    }
                }
            }
        }.start();
    }

    private void closeWhenDone() {
        this.dispose();
        new FrmMethod().setVisible(true);
    }

